#include "libft.h"

char	*ft_strchr(const char *s1, int c)
{
	int		i;
	char	*s1_1;

	i = 0;
	s1_1 = (char *)s1;
	while (s1_1[i] != '\0' && s1_1[i] != c)
		i++;
	if (s1_1[i] == c)
		return (&s1_1[i]);
	return (NULL);
}
