#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int				i;
	unsigned char	*s;

	i = 0;
	s = (unsigned char *)src;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
