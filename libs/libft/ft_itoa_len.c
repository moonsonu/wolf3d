#include "libft.h"

int		ft_itoa_len(int n)
{
	int		len;
	int		negative;

	len = 1;
	negative = 0;
	if (n < 0)
	{
		n *= -1;
		negative = 1;
	}
	len += negative;
	while (n /= 10)
		len++;
	return (len);
}
