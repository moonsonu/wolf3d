#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		l;
	char	*s2_2;

	i = 0;
	l = 0;
	s2_2 = (char *)s2;
	while (s1[i] != '\0')
		i++;
	while (s2_2[l] != '\0')
	{
		s1[i] = s2_2[l];
		l++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
