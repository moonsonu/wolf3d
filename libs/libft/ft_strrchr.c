#include "libft.h"

char	*ft_strrchr(const char *s1, int c)
{
	char	*s1_1;
	int		i;

	s1_1 = (char *)s1;
	i = ft_strlen(s1_1);
	while (i != 0 && s1_1[i] != c)
		i--;
	if (s1_1[i] == c)
		return (&s1_1[i]);
	return (0);
}
