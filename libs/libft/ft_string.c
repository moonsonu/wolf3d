#include "libft.h"

char		*ft_string(char const *s, char c)
{
	char	*w;
	int		i;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	w = (char*)malloc(sizeof(char) * (i + 1));
	i = 0;
	while (s[i] && s[i] != c)
	{
		w[i] = s[i];
		i++;
	}
	w[i] = '\0';
	return (w);
}
