#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*a;
	size_t			s;

	s = 0;
	a = (unsigned char *)b;
	while (s < len)
	{
		*a = c;
		a++;
		s++;
	}
	return (b);
}
