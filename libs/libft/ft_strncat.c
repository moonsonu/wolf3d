#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	l;
	char	*s2_2;

	i = 0;
	l = 0;
	s2_2 = (char *)s2;
	while (s1[i] != '\0')
		i++;
	while (l < n && s2_2[l] != '\0')
	{
		s1[i] = s2_2[l];
		i++;
		l++;
	}
	s1[i] = '\0';
	return (s1);
}
